#include <stdio.h>

int	main()
{
	char	matrix[15][15] = {
	{"......o........"},
	{".....o........."},
	{".........o....."},
	{".......o....oo."},
	{"..............."},
	{"..............."},
	{".........o....."},
	{"..............."},
	{"..............."},
	{"..............."},
	{"o...o.........."},
	{"o..o....o.....o"},
	{"..............."},
	{".............o."},
	{"....o.........."},
	};
	int count = 0;
	int i = 0;
	
	while (i < 13)
	{
		int j = 0;
		while (j < 13)
		{
			if (matrix[i][j] == '.' && matrix[i][j + 1] == '.' && matrix[i][j + 2] == '.'
				&& matrix[i + 1][j] == '.' && matrix[i + 1][j + 1] == '.' && matrix[i + 1][j + 2] == '.'
					&& matrix[i + 2][j] == '.' && matrix[i + 2][j + 1] == '.' && matrix[i + 2][j + 2] == '.')
				count++;
			j++;
		}
		i++;
	}
	printf("%d\n", count);
	return (0);
}
